package com.blitline.image.spring.web;

public class FooHolder {
    private int foo;

    public FooHolder() {
    }

    public FooHolder(int foo) {
        this.foo = foo;
    }

    public int getFoo() {
        return foo;
    }

    public void setFoo(int foo) {
        this.foo = foo;
    }
}
